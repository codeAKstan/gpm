<p>Despite a false break above in late February, pair is essentially capped by the 23.6 Fib. Above the 23.6 we have the 38.2, a break above the 23.6 would likely encounter resistance here. These levels are very common; a trend can retrace these percentages and still be considered healthy. Next we have the 50% mark, note this level is important as it coincides with former support. Above 50% and traders are beginning to question the recent move – is this a retracement or a reversal? Having said that; 61.8 retracements, prior to continuation are also fairly common. The 78.6 is considered the be all and end all – if a pair retraces more than 78.6% of the prior move, chances are it’s heading straight back to the origin (100%). It may have completely reversed direction or is range bound. </p>
<p>A trader with a bearish bias on USDCAD would wait for a break above the 23.6 and look for a reaction at one of the higher fib levels, before entering short. On the other hand, a trader with a bullish bias might trade a break above the 23.6 with a stop below the recent lows, or wait for a break above one of the higher fibs. </p>
<p>The previous examples were all assessing down moves; let’s take a look at an up move:</p>
<p><img loading="lazy" src="wp-content/uploads/2016/05/education_post_img29.jpg" alt="education_post_img29" width="451" height="225" class="aligncenter size-full wp-image-18678" /></p>
<p>The process is the same, but instead of dragging the Fibonacci tool from the high to the low; we drag it from the low to the high. A trader looks at this chart and see’s USDCHF is trending up and decides they want to buy. They know not to buy into resistance, so they use the Fibonacci tool to identify probable support levels. They notice that price seems to be respecting the 50% level and decide they will attempt to enter long there. They have two options:</p>
<p>1) Watch and wait for a correction to, reaction at the 50% level<br />
2)Place a Buy Limit order just above the 50% level</p>
<p>The first option is probably ‘safer’, as the trader is not blindly buying into a potential support level. On the other hand, the second option means the trader does not have to sit and watch the market. Either way, our trader has four options for placing their stop, depending on their risk tolerance:</p>
<ol>
<li>A tight stop just below the 50% level</li>
<li>A more reasonable stop below the 61.8</li>
<li>A considerably looser stop below the 78.6</li>
<li>A stop below 100 – if price moves below here the trader’s bullish bias is unquestionably invalidated as pair has set a new low</li>
</ol>
<p>The first option is likely a little too risky, most traders would probably opt for the balanced choice under the 61.8. With the latter two, the trader risks holding on to a losing position for longer than necessary and is sacrificing reward.</p>
<p>Fibonacci Retracements are a great way of identifying potential support and resistance levels. When analysing a down move, the trader uses the tool from the high to the low. When looking at a rally, the trader drags from the low to the high. The standard 23.6, 38.2 and 61.8 Fib levels are great, but many Fibonacci traders add in the 78.6 and 50 levels too. Trade breaks of key Fibs or reactions, depending on your strategy and bias.  </p>
