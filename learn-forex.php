<p><strong>Monetary Policy</strong> is the process of setting the interest rate and controlling the supply of money.</p>
<p>There are three main goals that monetary policy helps a central bank achieve.  Those being:</p>
<ul>
<li>Economic growth targets.</li>
<li>Inflation in the target band.</li>
<li>Low unemployment.</li>
</ul>
<p><img loading="lazy" src="wp-content/themes/vfx_new/images/education_post_img23.jpg" alt="education_post_img23" width="254" height="303" class="aligncenter size-full wp-image-18664" /></p>
<p>Now we have gone through what monetary policy is, let’s go one step further.  Have you ever heard monetary policy referred to as being Hawkish or Dovish?  No, economists aren&#8217;t watching birds or performing magic tricks, they&#8217;re actually describing the types of monetary policy that central banks have at their disposal.</p>
<p><strong>Hawkish monetary policy</strong> is indicative of rising interest rates.  It is sometimes referred to as tightening because essentially the central bank is looking to tighten the economy and slow it down in the wake of higher inflation.</p>
<p>Under a hawkish scenario where interest rates are rising, the borrowing of money by both business and consumers becomes more expensive (due to higher interest repayments) so spending and investment decreases as a result.</p>
<p><strong>Dovish monetary policy</strong> is the opposite, and indicative of falling interest rates.  A central bank may decide to loosen monetary policy by cutting interest rates with the goal of stimulating a stagnating economy in mind.</p>
<p>Under a dovish scenario where interest rates are falling, money is being made cheaper and more accessible to business and consumers which encourages them to invest or spend.  This spending then stimulates a stagnant economy.</p>
<p>By using the different types of monetary policy, a central bank can somewhat control, and in the end smooth the wild swings between the good and bad times experienced by an economy.  Both economies and markets want stability and there is no point in experiencing huge booms if they are followed by equally as devastating busts in a regular business cycle.</p>
