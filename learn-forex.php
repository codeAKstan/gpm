<p>Divergence occurs when a <a href="learn-forex.php#">forex currency pair</a> makes a new high or low and the indicator (in this case RSI) does not confirm:</p>
<p><img loading="lazy" src="wp-content/themes/vfx_new/images/education_post_img55.jpg" alt="education_post_img55" width="451" height="225" class="aligncenter size-full wp-image-18729" /></p>
<p>This is a 4 hour chart of USD/CHF which shows both bullish and bearish RSI divergence. <strong>Bullish divergence</strong> occurs when price move to new lows, while RSI carves a higher low. </p>
<p>Conversely, bearish divergence occurs when price moves to new highs, but RSI stops short of the previous peak. We’ve highlighted bullish (buy) signals in green and bearish (sell) signals in red:</p>
<p><img loading="lazy" src="wp-content/themes/vfx_new/images/education_post_img56.jpg" alt="education_post_img56" width="451" height="225" class="aligncenter size-full wp-image-18730" /></p>
<p>The buy signal (green), yields over 200 pips before a basic overbought cross tells the forex trader to exit. Note that there is a slight pause between this exit signal and the bearish divergence signal. This move is yet to play out, but the forex pair has already declined 90 pips. Let’s take a closer look:</p>
<p><img loading="lazy" src="wp-content/themes/vfx_new/images/education_post_img57.jpg" alt="education_post_img57" width="451" height="225" class="aligncenter size-full wp-image-18731" /></p>
<p>Note that before the bullish divergence signal, RSI issues 3 basic buy signals as it crosses back above 30 (red crosses). The first two are pretty shocking; USD/CHF declines over 250 pips before eventually turning higher. The third signal is comparably better – the pair only declines around 115 pips before the bullish reversal. Conversely, the bullish divergence signal coincides perfectly with the actual bottom.</p>
<p>There is only one basic sell signal before the bearish divergence – USD/CHF appreciates another 50 pips before the top – this is not as bad as the false buy signals, but there’s always room for improvement. This time, the divergence signal does not coincide with the top perfectly – but the difference is negligible – approximately 5 pips.</p>
<p>A forex trader taking basic RSI signals on their <a href="https://gpmautomatedtradingsystem.com/forex-trading-platforms/index.php">MT4 platform</a> would have had at least 1 losing trade with this example (possibly 3 depending on their stop loss). On the other hand, a divergence forex trader would have netted two winners and not lost a single trade.   </p>
<p>The take away: A forex trader who waits for divergence, will likely avoid some losing trades and enter the market at a better price than your basic RSI trader. This allows for tighter stops and easier targets and should yield an improved win rate. RSI often precedes moves in price.  Meaning that RSI will often begin to trend up/down before price does – this can give the divergence forex trader a profitable head start over your average price action forex trader.</p>
