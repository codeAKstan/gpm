                            <h4 class="title">Fibonacci</h4>
							<p>Fibonacci retracements are a quick and easy way of predicting support and resistance levels in Forex. The Fibonacci tool works on the principle that markets tend to ‘retrace’ a portion of a move prior to continuing the dominant trend. Traders use the Fibonacci tool on <a href="https://gpmautomatedtradingsystem.com/forex-trading-platforms/metatrader-4/index.php" target="_blank" rel="noopener noreferrer">MT4</a> to connect the lows and highs of a recent trend, swing or range and the tool displays likely support and resistance levels derived from the Fibonacci Sequence (Golden Ratio). Fibonacci levels appear again and again in Forex &#8211; this tool is surprisingly reliable when it comes to predicting support and resistance:</p>
<p><img loading="lazy" src="wp-content/themes/vfx_new/images/education_post_img26.jpg" alt="education_post_img26" width="451" height="225" class="aligncenter size-full wp-image-18675" /></p>
<p>This is the recent down move in USDCAD. If we use the Fibonacci tool to connect the previous peak to the range low, we are shown three potential resistance levels. The standard Fibonacci retracement levels are 23.6, 38.2 &amp; 61.8, but many traders also use the 78.6 and 50 levels. 50 is not actually derived from the Fibonacci sequence, but the importance of the level cannot be denied – 50% retracements are very common place:</p>
<p><img loading="lazy" src="wp-content/themes/vfx_new/images/education_post_img27.jpg" alt="education_post_img27" width="451" height="225" class="aligncenter size-full wp-image-18676" /></p>
