<p>RSI looks at an instrument’s ability to close higher/lower over a given period. When RSI is above 70, a <a href="learn-forex.php#">forex currency pair</a> is considered ‘overbought’ and when it’s below 30, it’s considered ‘oversold’. It’s important to note that just because a security is overbought/oversold, does not mean it’s going to reverse – Indicators can read overbought/oversold for extended periods of time. A sell signal does not occur until RSI crosses back from overbought/oversold into the 70-30 range:</p>
