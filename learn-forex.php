                            <h4 class="title">How to Avoid a Margin Call and Forced Closure</h4>
							<p>Forex traders have the ability to leverage a small amount of capital and open positions hundreds of times larger than their account balance, unlocking the door to incredible profits. Leverage however, is a double-edged sword: with great profit potential, comes the potential for large losses. If you open too large a position, or too many smaller positions, and the coin-toss goes against you, you could face a margin call and forced closure, leaving you with a small fraction of your original balance. So how can you avoid a margin call and forced closure? </p>
<p><strong>Position Sizing</strong></p>
<p>Safe, calculated position sizing goes hand in hand with successful forex trading. Before entering a trade, you should know where you are going to place your stop loss and how much you are risking on the position &#8211; how far your stop loss is from your entry and how much you are risking per trade determines the size of your position. It should never be the other way around: the size of your position should not determine your stop loss, or risk per trade. </p>
<p>Some education outlets and gurus will tell you that it is OK to risk as much as 5% per trade. Most professional traders on the other hand, will tell you this is far too much risk for a single position. Imagine you are trying to keep your drawdown below 20%: if you are risking 5% per trade and lose 4 trades in a row, you have already met your drawdown tolerance. The more an account draws-down, the harder it is it to build back up. Large drawdowns are also very tough on the mind and you could begin to revenge-trade or open even larger positions in order to try and make back your losses &#8211; this is not trading &#8211; it is gambling in every sense of the word. </p>
<p>In general, you should never risk more than 2% of your account balance on any one trade. If you are just starting out, 1% might be more appropriate. Once you have learned the ropes and you are confident with yourself and your strategy, then you could consider increasing your position size a little. Either way though, 5% is probably too much for the majority of strategies. Even the best professional traders can have losing streaks well in excess 4 trades. </p>
<p>If you want to trade larger positions, you should fund your account appropriately. This is the only safe way of trading with size. </p>
<p><strong>Number of positions and correlation</strong></p>
<p>The number of positions you have open determines your risk at any one time. Just because you are only risking 2% per trade, don’t think you can go crazy and open 10 simultaneous positions &#8211; this is a sure-fire way to receive a margin call. </p>
<p>Even if you only have two positions open, but you are trading highly correlated markets, you are still essentially risking 4% on a single trade. An example of this would be risking 2% on a long AUDUSD position, whilst simultaneously risking 2% on a long NZDUSD position &#8211; if the USD surges, you will be stopped out of both positions simultaneously, and lose 4%. </p>
<p>On the flipside, don’t take opposite trades in highly correlated markets and assume your risk is zero. If we take the above example, except you go long AUDUSD and short NZDUSD; yes the USD components theoretically cancel each other out, but you still have long AUD and short NZD exposure. Also, correlated markets don’t always move in lockstep and in times of high volatility, the market can move violently in both directions in just a few minutes and take out both of your stops. </p>
<p>In general, you should never have more than two or three positions open at the same time, and you should try to avoid trading highly-correlated markets, or at least be aware of the risks. </p>
<p><strong>Margin call and forced closure</strong></p>
<p>If the equity in your account falls below 100% of your margin requirements, you will receive a margin call (in modern times this is just an email &#8211; no one will physically call you!). The margin call is informing you that you have insufficient equity in your account and you should either close some of your positions, or top-up your account with appropriate funds. You can close, or partially close your positions from your MT4 terminal, or log into your client area and top up your account with a credit card, or one of our other instant funding options. </p>
<p>If you ignore the margin call warning and your equity continues to fall, reaching 50%, your positions will be automatically closed and your floating losses will be realized. This is what’s known in trading circles as a ‘blown account’ &#8211; you will be left with a very small portion of your original balance. Exactly how little you are left with depends on how much leverage you were using and how many positions you had open. If you have lots of positions open, they won’t all be closed simultaneously, but progressively. This means you could be left with only a few dollars in your account. Blown accounts must be avoided at all costs. </p>
